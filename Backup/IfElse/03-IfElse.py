# Compund Assignment -> =
userMsg = input("Enter your message : ")

hello = ['hi','hello','hey','hie']

# Membership Operators - in, not in
# Comparison Operator -> ==, >, <, >=, <=
# Logical Operators -> AND, OR, NOT
if userMsg in hello:
    print("Hello")
elif userMsg == "bye":
    print("Bye")
else:
    print("I don't understand")
